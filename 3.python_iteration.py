# if...else and elif statements control
x = 'one'
if x == 0:
    print('False')
elif x == 1:
    print('True')
else:
    print('Something else')

# While loop
print('Example of a while loop')
x = 0
while x <= 3:
    print(x)
    x+=1

print('Example of a for loop')
words = ['cat', 'dog', 'elephant']
for word in words:
    print(word)

